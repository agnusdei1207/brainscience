import os
import unicodedata

def get_char_width(c):
    if c in '▶◀▼▲■': return 1
    if 0x2500 <= ord(c) <= 0x257F: return 1
    if unicodedata.east_asian_width(c) in ('W', 'F'): return 2
    return 1

def get_str_width(s):
    return sum(get_char_width(c) for c in s)

def align_ascii_box(text):
    lines = text.strip().split('\n')
    out = []
    in_box = False
    box_lines = []
    
    for line in lines:
        if line.startswith('```text'):
            out.append(line)
            in_box = True
        elif line.startswith('```') and in_box:
            max_w = 0
            for bl in box_lines:
                if bl.startswith('┌') or bl.startswith('├') or bl.startswith('└') or bl.startswith('│'):
                    inner = bl[1:-1] if bl[-1] in '┐┤┘│' else bl[1:]
                    w = get_str_width(inner.rstrip('─ '))
                    if w > max_w: max_w = w
                else:
                    w = get_str_width(bl)
                    if w > max_w: max_w = w
            
            max_w += 2 

            for bl in box_lines:
                if bl.startswith('┌'):
                    out.append('┌' + '─' * max_w + '┐')
                elif bl.startswith('├'):
                    inner = bl[1:-1] if bl[-1] == '┤' else bl[1:]
                    inner = inner.strip('─')
                    if inner:
                        w = get_str_width(inner)
                        out.append('├' + inner + '─' * (max_w - w) + '┤')
                    else:
                        out.append('├' + '─' * max_w + '┤')
                elif bl.startswith('└'):
                    out.append('└' + '─' * max_w + '┘')
                elif bl.startswith('│'):
                    inner = bl[1:-1] if bl[-1] == '│' else bl[1:]
                    inner = inner.rstrip()
                    w = get_str_width(inner)
                    out.append('│' + inner + ' ' * (max_w - w) + '│')
                else:
                    out.append(bl)
            
            out.append(line)
            in_box = False
            box_lines = []
        elif in_box:
            box_lines.append(line)
        else:
            out.append(line)
    return '\n'.join(out) + '\n'

def write_file(filename, content):
    aligned_content = align_ascii_box(content)
    filepath = f"content/studynote/9_security/2_crypto/{filename}"
    os.makedirs(os.path.dirname(filepath), exist_ok=True)
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(aligned_content)

